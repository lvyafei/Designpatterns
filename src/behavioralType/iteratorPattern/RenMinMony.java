package behavioralType.iteratorPattern;

public class RenMinMony {
	int value;
	
	private boolean isTure;
	
	public RenMinMony(int value,boolean isTure) {
	   this.value = value;
	   this.isTure = isTure;
	}
	
    public int getValue() {
	    return value;
    }
    
	public boolean isTure() {
	    return isTure;
	}
}
